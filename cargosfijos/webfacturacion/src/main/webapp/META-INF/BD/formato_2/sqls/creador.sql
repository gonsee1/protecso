	UPDATE #TMREPO_GRAL_002
	SET #TMREPO_GRAL_002.IS_NUEV = 'NO'
	WHERE #TMREPO_GRAL_002.CO_NEGO_SUCU IN 
		(SELECT CO_NEGO_SUCU FROM #TMNEGO_NUEV_003)

		
