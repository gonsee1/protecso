			,SUM([IM_MONTO]) [IM_MONTO]
		FROM #TMSERV_UNIC_SUPL_001
		GROUP BY [CO_NEGO_SUCU]) A
	WHERE #TMREPO_GRAL_002.CO_NEGO_SUCU = A.CO_NEGO_SUCU


